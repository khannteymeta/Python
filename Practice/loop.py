fruits = ["apple", "banana", "cherry"]


# using for loop
print("Using for loop:")
for i in range(len(fruits)):
    # print each fruit in the list
    print(f"Fruit : {fruits}")
    
    
    
# using while loop
print("Using while loop:")
i = 0
while i < len(fruits):
    print(f"Fruit : {fruits}")
    i += 1
    
  
    
# using for-each loop
print("Using for-each loop:")
for fruit in fruits:
    print(f"Fruit : {fruit}")    
    